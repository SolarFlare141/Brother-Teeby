function lightMode(){
    document.body.style.backgroundColor="#c0c0c0";
    document.body.style.color="black";
}
function darkMode(){
    document.body.style.backgroundColor="#242124";
    document.body.style.color="white";
}
function Reset(){
    document.body.style.backgroundColor="DarkOliveGreen";
    document.body.style.color="white";
}
